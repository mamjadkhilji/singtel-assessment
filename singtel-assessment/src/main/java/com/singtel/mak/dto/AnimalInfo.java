package com.singtel.mak.dto;

public class AnimalInfo { 
	
	private String animalType;
	private String animalSong;
	private String animalFly;
	private String animalSwim;
	private String animalWalk; 

	
	public String getAnimalType() {
		return animalType;
	}
	public void setAnimalType(String animalType) {
		this.animalType = animalType;
	}
	public String getAnimalSong() {
		return animalSong;
	}
	public void setAnimalSong(String animalSong) {
		this.animalSong = animalSong;
	}
	public String getAnimalFly() {
		return animalFly;
	}
	public void setAnimalFly(String animalFly) {
		this.animalFly = animalFly;
	}
	public String getAnimalSwim() {
		return animalSwim;
	}
	public void setAnimalSwim(String animalSwim) {
		this.animalSwim = animalSwim;
	}
	public String getAnimalWalk() {
		return animalWalk;
	}
	public void setAnimalWalk(String animalWalk) {
		this.animalWalk = animalWalk;
	}
	
	

}
